weather = "snowy"

case weather
when "sunny"
    puts "You should put on some sunglasses and go outside!"
when "cloudy"
    puts "Make sure you bring an umbrella in case it rains."
when "rainy"
    puts "Do you have an umbrella with you?"
when "snowy"
    puts "Bundle up, put on a hat and gloves, and make sure you wear your parka today."
when "foggy"
    puts "Remember to turn on the headlights on your car."
when "hot"
    puts "Drink a lot of water today so you stay hydrated."
when "cold"
    puts "Make some hot chocolate and stay inside."
else
    puts "I'm not sure what you should today with that type of weather, sorry."
end
