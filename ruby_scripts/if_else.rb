my_name = "Lynn"
if my_name == "Lynn"
    puts "Hello, Lynn!"
else 
    puts "Ooops, I thought your name was Lynn.  Sorry about that, #{my_name}."
end

fav_color = red
if (fav_color == red)
    puts "Red like fire!"
elsif (fav_color == orange)
    puts "Orange like an ...orange."
elsif (fav_color == yellow)
    puts "Yellow daffordils are so pretty in the spring!"
elsif (fav_color == green)
    puts "Have you been to the Emerald City in OZ?"
elsif (fav_color == blue)
    puts "Blue like the sky!"
elsif (fav_color == purple)
    puts "Purple plums are totally the tastiest."
else 
    puts "I don't know what color that is, sorry!"
end
